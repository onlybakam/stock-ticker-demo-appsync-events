import type { Handler } from 'aws-lambda'

const MAX_INDEX = 390
const HTTP_ENDPOINT = `https://${process.env.HTTP_ENDPOINT!}/event`
const REAL_ENDPOINT = `wss://${process.env.REAL_ENDPOINT!}/event/realtime`
const API_KEY = process.env.API_KEY!
const WAIT_TIME = process.env.WAIT_TIME ? +process.env.WAIT_TIME : 1

export const handler: Handler = async (event) => {
  let index = 0
  while (index < MAX_INDEX) {
    await getLatestPrice(index)
    index += 1
    await sleep(WAIT_TIME * 1_000)
  }
  return true
}

const round = (number: number) => (number ? +number.toFixed(2) : null)
const sleep = (ms: number) => new Promise((resolve) => setTimeout(resolve, ms))
async function getLatestPrice(index: number) {
  try {
    const stock = data.chart.result[0]
    const quote = stock.indicators.quote[0]
    const price = {
      x: stock.timestamp[index] * 1_0000,
      y: [quote.open[index], quote.high[index], quote.low[index], quote.close[index]].map(round),
    }
    console.log(price)

    const resp = await fetch(HTTP_ENDPOINT, {
      method: 'POST',
      headers: { 'x-api-key': API_KEY },
      body: JSON.stringify({
        channel: '/stocks/TICKER',
        events: [JSON.stringify(price)],
      }),
    })
  } catch (error) {
    console.log(error)
  }
}

